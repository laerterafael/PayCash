    public void LoadTableCargo(){
        DefaultTableModel modelo = (DefaultTableModel) tbl_cargo.getModel();
        modelo.setNumRows(0);
        CargoDAO cdao = new CargoDAO();

        for (Cargo c: cdao.read()) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getCodigo(),
                c.getNome(),
            });

        }
 
    }
    
